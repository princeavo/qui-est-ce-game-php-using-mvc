        <h2>Les règles de notre jeu</h2>
        <div id="description">
            <p>Notre jeu est un jeu de devinette.</p>
            <p>Vous avez à choisir deux modes de jeu: soit deviner les animés ou deviner les célébrités.</p>
            <p>Au début du jeu nous vous attribuons 20points.</p>
            <p>Vous aurez aussi le choix entre trois niveaux de difficultés croissante I,II et III.</p>
            <p>Selon le mode de jeu,vous verrez une liste de photos de célébrités ou une liste de photos de personnages animées.</p>
            <p>Quand vous cliquez sur une photo la photo disparaît</p>
            <p>Pour choisir une bonne réponse,vous rentrez le nom de votre choix dans le champ de texte qui est juste en bas de la page de jeu.</p>
            <p>Si votre réponse n'est pas la bonne nous vous soutirons 1 point.</p>
            <p>Nous vous offrons des indices pour vous situer dans votre choix.</p>
            <p>Quand vous demandez un indice nous vous soutirons le point représentant correspondant à votre niveau(1 pour le niveau I,2 pour le niveau 2 et 3 pour le niveau 3).</p>
            <p>Une fois que vous donner la bonne réponse nous enregistrons votre score (Nombre de points restants) et nous vous affichons le score le plus élevé par niveau</p>
            <p>Nous demandons juste vos informations afin de pouvoir enregistrer vos scores. Assurez-vous de la sécrité de vos informations personneles</p>
            <p><b>Quand vous abandonnez un jeu en cours votre score est mis à 0</b></p>
            <p><a  href="../#jouer">Jouer maintenant</a></p>
        </div>
